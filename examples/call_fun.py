# abs函数 求绝对值  在交互式命令行通过help(abs)查看abs函数的帮助信息
x = abs(100)
y = abs(-20)
z = abs(12.34)
print(x, y, z)

print('max(1, 2, 3) =', max(1, 2, 3))
print('min(1, 2, 3) =', min(1, 2, 3))
print('sum([1, 2, 3]) =', sum([1, 2, 3]))

print('int(\'123\'):', int('123'))
print('int(12.34):', int(12.34))
print('float(\'12.34\'):', float('12.34'))
print('str(1.23):', str(1.23))
print('str(100):',str(100))
print('bool(1):',bool(1))
print('bool(\'\'):',bool(''))

# hex 把一个整数转换成十六进制表示的字符串
print('hex(255):',hex(255))
print('hex(1000):',hex(1000))
